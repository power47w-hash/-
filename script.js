// ====== دوال الإيموجي والرموز ======
function displayEmojis(category = 'all') {
    const grid = document.getElementById('emoji-grid');
    if (!grid) return;
    const emojisToShow = emojiDatabase[category] || emojiDatabase.all;
    grid.innerHTML = '';
    emojisToShow.forEach(emoji => {
        const emojiDiv = document.createElement('div');
        emojiDiv.className = 'emoji-item';
        emojiDiv.textContent = emoji;
        emojiDiv.onclick = () => copyToClipboard(emoji);
        grid.appendChild(emojiDiv);
    });
}

function displaySymbols(category = 'all') {
    const grid = document.getElementById('symbols-grid');
    if (!grid) return;
    const symbolsToShow = symbolsDatabase[category] || symbolsDatabase.all;
    grid.innerHTML = '';
    symbolsToShow.forEach(symbol => {
        const symbolDiv = document.createElement('div');
        symbolDiv.className = 'emoji-item';
        symbolDiv.textContent = symbol;
        symbolDiv.onclick = () => copyToClipboard(symbol);
        grid.appendChild(symbolDiv);
    });
}

function setupCategoryButtons() {
    const emojiCategoryBtns = document.querySelectorAll('#emoji-categories .category-btn');
    if (emojiCategoryBtns.length > 0) {
        emojiCategoryBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                emojiCategoryBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                displayEmojis(btn.dataset.category);
            });
        });
    }
    
    const symbolsCategoryBtns = document.querySelectorAll('#symbols-categories .category-btn');
    if (symbolsCategoryBtns.length > 0) {
        symbolsCategoryBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                symbolsCategoryBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');
                displaySymbols(btn.dataset.category);
            });
        });
    }
}

// ====== دوال تعديل الأسماء ======
function createStylesList() {
    const stylesList = document.getElementById('styles-list');
    if (!stylesList) return;
    
    stylesList.innerHTML = '';
    styleTemplates.forEach((style, index) => {
        const card = document.createElement('div');
        card.className = 'style-card';
        card.onclick = () => copyStyledText(index);
        card.innerHTML = `
            <span class="style-icon">${style.icon}</span>
            <span class="style-content" id="style-${index}">${style.converter('POWER')}</span>
            <span class="copy-hint">نسخ</span>
        `;
        stylesList.appendChild(card);
    });
}

function updateAllStyles(text) {
    if (!text) return;
    for (let i = 0; i < styleTemplates.length; i++) {
        const element = document.getElementById(`style-${i}`);
        if (element) {
            element.textContent = styleTemplates[i].converter(text);
        }
    }
}

function copyStyledText(index) {
    const nameInput = document.getElementById('name-input');
    const currentText = nameInput?.value.trim() || 'POWER';
    const styledText = styleTemplates[index].converter(currentText);
    copyToClipboard(styledText);
}

function toggleResults(show) {
    const resultsSection = document.getElementById('results-section');
    const welcomeMessage = document.getElementById('welcome-message');
    const typingStats = document.getElementById('typing-stats');
    
    if (resultsSection && welcomeMessage && typingStats) {
        if (show) {
            resultsSection.classList.add('visible');
            welcomeMessage.style.display = 'none';
            typingStats.classList.add('visible');
        } else {
            resultsSection.classList.remove('visible');
            welcomeMessage.style.display = 'block';
            typingStats.classList.remove('visible');
        }
    }
}

function updateCharCount(text) {
    const charCount = document.getElementById('char-count');
    if (charCount) {
        charCount.textContent = text.length;
    }
}

// ====== دوال النسخ العامة ======
function copyToClipboard(text) {
    navigator.clipboard.writeText(text).then(() => {
        showToast();
    }).catch(() => {
        const textarea = document.createElement('textarea');
        textarea.value = text;
        document.body.appendChild(textarea);
        textarea.select();
        document.execCommand('copy');
        document.body.removeChild(textarea);
        showToast();
    });
}

function showToast() {
    const toast = document.getElementById('copy-toast');
    if (!toast) return;
    toast.classList.add('show');
    setTimeout(() => {
        toast.classList.remove('show');
    }, 1500);
}

